num1 = 3
num2 = 2

# Airthmetic operators
print(num1+num2)
print(num1-num2)
print(num1*num2)
print(num1/num2)
print(num1//num2)
print(num1**num2)
print(num1%num2)

# Comparison operators
print(num1==num2)
print(num1!=num2)
print(num1>num2)
print(num1<num2)
print(num1>=num2)
print(num1<=num2)

# Logical Operators

age = 20
print(age>=18 and age<=60)
print(age>=18 or age<=60)
print(not(age>=18 and age<=60))

